                    Кроме того, предусмотрено дополнительное требование о наличии опыта работы не менее 2 лет,
                    в течение которых под руководством кадастрового инженера кандидат участвовал в подготовке и
                    проведении кадастровых работ. Также придется заключить договор обязательного страхования гражданской
                    ответственности кадастрового инженера. Минимальная страховая сумма - 2,5 млн руб.
                    Квалификационный экзамен проводится в форме тестирования с применением автоматизированной
                    информационной системы. Его принимает специальная комиссия, формируемая национальным объединением
                    СРО кадастровых инженеров. Членство кадастрового инженера в одной из СРО стало обязательным.
                    Прописана процедура вступления в СРО и исключения из нее, а также правила прохождения стажировки
                    в качестве помощника кадастрового инженера. Кроме того, закреплены права и обязанности кадастрового
                    инженера. Так, он может отказаться от выполнения кадастровых работ в случаях, если заказчик нарушил
                    условия договора, не обеспечил предоставление необходимой информации и (или) документов.
                    Среди обязанностей - 1 раз в 3 года повышать свою квалификацию. Более детально регламентированы
                    вопросы деятельности СРО кадастровых инженеров. Введены новые статьи,
                    посвященные нацобъединению СРО, госреестру СРО, госнадзору в данной сфере.
                    Согласно поправкам решение о приостановлении процедуры кадастрового учета может быть оспорено в
                    суде только после его обжалования в апелляционной комиссии, созданной при органе кадастрового учета.
                    Урегулирован порядок формирования и работы такой комиссии. Федеральный закон вступает
                    в силу с 1 июля 2016 г., за исключением отдельных положений, для которых установлены иные сроки.</p>
