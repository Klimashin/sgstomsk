                <h3>Геодезия и картография: новый закон</h3>
                <p>Федеральный закон от 30 декабря 2015 г. № 431-ФЗ “О геодезии, картографии и пространственных
                    данных и о внесении изменений в отдельные законодательные акты Российской Федерации”</p>
                <p>Принят новый Закон о геодезии, картографии и пространственных данных. В частности, установлено,
                    что для обеспечения геодезических и картографических работ создаются государственные геодезическая,
                    нивелирная и гравиметрическая сети. Правообладатели объектов недвижимости, на которых
                    размещены пункты вышеуказанных сетей, обязаны обеспечивать их сохранность и уведомлять
                    уполномоченный орган обо всех случаях их повреждения или уничтожения. Кроме того, они должны
                    предоставлять возможность подъезда (подхода) к ним для геодезических и картографических работ,
                    а также при проведении ремонта и восстановления пунктов. Отдельная глава закона посвящена федеральным,
                    ведомственным и региональным фондам пространственных данных, а также фонду таких данных, используемых
                    в целях обеспечения обороны. Предусмотрено создание федерального и региональных порталов
                    пространственных данных. Заинтересованные лица смогут получить к ним доступ через Интернет.
                    Также организуется государственная информационная система ведения единой электронной картографической
                    основы. Последняя представляет собой упорядоченную совокупность пространственных данных
                    о территории страны. За пользование данным ресурсом взимается плата. Регламентированы вопросы
                    госрегулирования геодезической и картографической деятельности (в т. ч. лицензирование,
                    госнадзор, ограничения на выполнение соответствующих работ). Сопутствующие поправки внесены
                    в ряд законодательных актов. Федеральный закон вступает в силу с 1 января 2017 г.,
                    за исключением отдельных норм, для которых установлены иные сроки.
                    Предусмотрены переходные положения.</p>
